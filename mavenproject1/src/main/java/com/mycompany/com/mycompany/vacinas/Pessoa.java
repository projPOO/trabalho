package com.mycompany.com.mycompany.vacinas;



public class Pessoa {
     public String nome;
     public String cpf;
     public int idade;
     
    public Pessoa() {
    }
 
    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getCpf() {
        return cpf;
    }

    public void setCpf(String cpf) {
        this.cpf = cpf;
    }

    public int getidade() {
        return idade;
    }

    public void setidade(int idade) {
        this.idade = idade;
    }


}

